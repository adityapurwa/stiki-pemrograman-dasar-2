public class TugasString_1_1 {
    public static void main(String[] args) {
        soal();
    }

    public static void soal() {
        // Digunakan untuk menggabungkan dua buah string
        System.out.println("Vector".concat("Groza"));

        // Digunakan untuk mencari index huruf 'a' terakhir
        System.out.println("Avaga".lastIndexOf('a'));

        // Digunakan untuk mencari di string tersebut ada substring "tan" atau tidak
        System.out.println("Titanic".contains("tan"));

        // Digunakan untuk mengecek apakah string tersebut kosong
        System.out.println("".isEmpty());

        // Digunakan untuk menghapus whitespace di awal dan di akhir string
        System.out.println(" Robinson ".trim());
    }

}
